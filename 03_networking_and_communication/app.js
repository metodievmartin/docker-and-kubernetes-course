// Cross-container communication can be done using the container's IP address or
// by creating a Docker network and running the containers within the same network.
// The IP address of a container can be obtained by running
// `docker inspect container_name`,
// However, this is not very convenient and can be brittle if the container is
// restarted and its IP address changes.
// The other option is to create a Docker network and run the containers within
// the same network. This allows us to use the container's name to communicate
// with each other.
mongoose.connect(
  'mongodb://mongodb-container:27017/swfavorites',
  { useNewUrlParser: true },
  (err) => {
    if (err) {
      console.log(err);
    } else {
      app.listen(3001);
    }
  }
);
